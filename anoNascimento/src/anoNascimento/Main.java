package anoNascimento;

import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int anoNascimento, anoAtual, idade;
		
		System.out.print("Digite o seu ano de nascimento: ");
		anoNascimento = sc.nextInt();
		System.out.print("Digite o ano atual: ");
		anoAtual = sc.nextInt();
		
		idade = anoAtual - anoNascimento;
		
		if(idade >= 18) {
			System.out.print("Você já pode tirar a carteira de habilitação\n");
		}
		else if (idade >= 16) {
			System.out.print("Você já tem idade para votar");
		}
		else if(idade >= 16 && idade <= 18) {
			System.out.print("Você já pode votar mais não pode tirar carteira de habilitação");
		}else if(idade < 16) {
			System.out.print("Você não pode votar e não pode tirar carteira de habilitação");
		}
		sc.close();

	}

}
